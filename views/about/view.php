                            <p class="text-2">Maktabgacha taʼlim tizimini yana-da takomillashtirish, sog‘lom, faol, barkamol, ijtimoiy moslashtirilgan bola shaxsini, shuningdek, kelgusi muvaffaqiyatli o‘quv faoliyati uchun shart-sharoitlarni shakllantirish va bolalarni boshlang‘ich taʼlimga sifatli tayyorlash maqsadida Vazirlar Mahkamasi qaror qiladi:

                                1. O‘zbekiston Respublikasi Maktabgacha taʼlim vazirligi, Qoraqalpog‘iston Respublikasi Vazirlar Kengashi, Farg‘ona viloyati va Toshkent shahar hokimliklarining 2018-2019 o‘quv yilidan boshlab Qoraqalpog‘iston Respublikasi, Farg‘ona viloyati tumanlarida (shaharlarida) va Toshkent shahri tumanlarida 1-ilovaga muvofiq ro‘yxat bo‘yicha bolalarni boshlang‘ich taʼlimga majburiy bepul bir yillik tayyorlashni joriy etish tajriba loyihasini amalga oshirish to‘g‘risidagi taklifiga rozilik berilsin.

                                2. Quyidagilar bolalarni boshlang‘ich taʼlimga majburiy bir yillik tayyorlashni joriy etish tajriba loyhasini (keyingi o‘rinlarda tajriba loyihasi deb ataladi) amalga oshirishda O‘zbekiston Respublikasi Maktabgacha taʼlim vazirligining asosiy vazifalari etib belgilansin:

                                maktabgacha taʼlim bilan qamrab olinmagan bolalar sirasiga kiradigan bolalarning majburiy bepul bir yillik tayyorlov guruhlarini (keyingi o‘rinlarda bolalar tayyorlov guruhlari deb ataladi) shakllantirish;

                                bolalar tayyorlov guruhlarining o‘quv-tarbiya jarayoniga zamonaviy innovatsion pedagogik texnologiyalarni, taʼlim va tarbiyaning samarali shakllari va usullarini joriy etish;

                                bolalar tayyorlov guruhlarini milliy madaniy-tarixiy qadriyatlarni aks ettiradigan va bolalikdan kitob o‘qishga qiziqishni singdiradigan o‘quv-metodik, didaktik materiallar, rivojlantiruvchi o‘yinlar va o‘yinchoqlar, badiiy adabiyotlar bilan taʼminlash;

                                maktabgacha taʼlim bilan qamrab olinmagan bolalarning ilg‘or xorijiy tajribani hisobga olgan holda davlat talablariga muvofiq har tomonlama aqliy, axloqiy, estetik va jismoniy kamol topishi uchun shart-sharoitlar yaratish;

                                davlat va nodavlat maktabgacha taʼlim muassasalari, maktabgacha taʼlim sohasida nodavlat taʼlim xizmatlari ko‘rsatish bilan shug‘ullanuvchi yuridik shaxslarning moddiy-texnika bazasini mustahkamlash.

                                3. Belgilansinki:

                                majburiy tayyorlov guruhlari uchun 2018-2019 o‘quv yili 2018 yil 10 dekabrdan boshlanadi va 2019 yil 10 avgustda tamomlanadi;

                                davlat va nodavlat maktabgacha taʼlim muassasalari, maktabgacha taʼlim sohasida nodavlat taʼlim xizmatlari ko‘rsatuvchi yuridik shaxslarning negizida to‘liq kun, to‘liq bo‘lmagan kun (bir haftada kamida 15 soat taʼlim-tarbiya jarayoni tashkil etilgan holda) tayyorlov guruhlari tarbiyalanuvchilari boshlang‘ich taʼlimga tayyorgarlikdan o‘tgan deb hisoblanadi va bolalarni boshlang‘ich taʼlimga majburiy bepul bir yillik tayyorlashdan ozod etiladi;

                                davlat va nodavlat maktabgacha taʼlim muassasalarida, maktabgacha taʼlim sohasida nodavlat taʼlim xizmatlari ko‘rsatuvchi yuridik shaxslarda boshlang‘ich taʼlimga majburiy yillik tayyorgarlikdan o‘tgan bolalarga boshlang‘ich taʼlimga majburiy bir yillik tayyorgarlikdan o‘tganlik to‘g‘risida davlat namunasidagi sertifikat beriladi;

                                boshlang‘ich taʼlimga majburiy bir yillik tayyorgarlikdan o‘tganlik to‘g‘risida sertifikat berish tartibi O‘zbekiston Respublikasi Maktabgacha taʼlim vazirligi tomonidan belgilanadi.

                                4. Qoraqalpog‘iston Respublikasi Vazirlar Kengashi, Farg‘ona viloyati va Toshkent shahar hokimliklariga, O‘zbekiston Respublikasi Maktabgacha taʼlim vazirligiga:

                                a) bolalar tayyorlov guruhlari uchun xonalarni tayyorlash, zarur bo‘lsa, yangi binolar, xonalar qurish yoki mavjudlarini kapital taʼmirlash, suv va elektr energiyasi hajmini o‘lchaydigan va hisobga oladigan alohida moslamalar o‘rnatish to‘g‘risida takliflar kiritilishini;

                                b) bir hafta muddatda:

                                bolalar tayyorlov guruhlari ro‘yxatlari tasdiqlanishini;

                                xonalarni o‘quv-metodik, didaktik materiallar, badiiy adabiyotlar, rivojlantiruvchi o‘yinlar va o‘yinchoqlar bilan taʼminlash yuklansin.

                                5. Quyidagilar:

                                Bolalarni boshlang‘ich taʼlimga majburiy bir yillik bepul tayyorlash tajriba guruhlari faoliyatini tashkil etish tartibi to‘g‘risidagi nizom 2-ilovaga muvofiq;

                                bolalarni boshlang‘ich taʼlimga majburiy bir yillik tayyorlash guruhlarini moddiy-texnika baza va taʼlim-didaktik materiallar bilan jihozlashning namunaviy normativlari 3-ilovaga muvofiq;

                                bolalar boshlang‘ich taʼlimga majburiy bir yillik tayyorgarlikdan o‘tganligi to‘g‘risida davlat namunasidagi sertifikat 4-ilovaga muvofiq tasdiqlansin.

                                6. O‘zbekiston Respublikasi Maktabgacha taʼlim vazirligi bir hafta muddatda bolalar tayyorlov guruhlarini malakali pedagog kadrlar bilan taʼminlasin.

                                7. O‘zbekiston Respublikasi Vazirlar Mahkamasi huzuridagi Taʼlim sifatini nazorat qilish davlat inspeksiyasi O‘zbekiston Respublikasi Maktabgacha taʼlim vazirligi bilan birgalikda taʼlim-tarbiya jarayonining sifatini nazorat qilsin hamda davlat va nodavlat maktabgacha taʼlim muassasalari va maktabgacha taʼlim sohasida nodavlat taʼlim xizmatlarini ko‘rsatish bilan shug‘ullanuvchi yuridik shaxslarning negizida bolalarni majburiy tayyorlash guruhlari faoliyati tashkil etilishi ustidan monitoring olib borsin.</p>
